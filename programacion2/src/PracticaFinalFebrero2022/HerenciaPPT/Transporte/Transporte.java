package PracticaFinalFebrero2022.HerenciaPPT.Transporte;

public class Transporte {

    String tipo;
    int capacidad_pas;

    public Transporte() {
        this.tipo = tipo;
        this.capacidad_pas = capacidad_pas;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getCapacidad_pas() {
        return capacidad_pas;
    }

    public void setCapacidad_pas(int capacidad_pas) {
        this.capacidad_pas = capacidad_pas;
    }
}
